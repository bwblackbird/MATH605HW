# Q: Briefly explain how it is entirely possible that 95% of the observations
# fall outside of the 95% CI.

# A: The confidence interval only gives a range of where the true mean 
# is likely to be. In sample data with a high variance, it is possible that
# 95% of observations will lie outside of the 95% confidence interval, since
# the 95% CI only describes where the true mean is likely to be 95% of the time.